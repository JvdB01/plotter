# settings file

debug     = False # sets the debug mode, can usually be ignored unless the menu is disabled
menu      = True  # decides whether or not the menu appears at the beginning of every run, if disabled, the settings will be read from this file
showdebug = False # decides whether or not to show and allow the debug option
minmax    = True  # decides whether to set the minimum values for the y axis (to prevent the axis from being out of proportion
yminscale = 1     # the scale of the bottom y axis compared to the x axis
ymaxscale = 1     # the scale of the top y axis compared to the x axis
hardlock  = False # decides whether or not the graph is locked to the minimum and maximum y values
verbose   = True  # decides whether or not to mention every whole point calculated, probably disable for huge graphs
